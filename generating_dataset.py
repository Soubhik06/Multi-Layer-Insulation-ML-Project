import pandas as pd
import random

materials = {
    "Polyurethane Foam": 0.03,
    "Fiberglass": 0.04,
    "Mineral Wool": 0.045,
    "Air": 0.025,
    "Ceramic Fiber": 0.06,
    "Concrete": 1.4,
    "Glass": 0.8,
    "Wood": 0.12
}

num_samples = 1000
min_layers = 1
max_layers = 5

data = []

for _ in range(num_samples):
    delta_T = random.uniform(50, 300)
    num_layers = random.randint(min_layers, max_layers)

    row = {"delta_T": delta_T}
    total_resistance = 0.0

    for i in range(1, max_layers + 1):
        if i <= num_layers:
            material = random.choice(list(materials.keys()))
            thickness = round(random.uniform(0.01, 0.1), 3)
            conductivity = materials[material]
            resistance = thickness / conductivity
            total_resistance += resistance

            row[f"Mat{i}"] = material
            row[f"L{i}"] = thickness
            row[f"k{i}"] = conductivity
        else:
            row[f"Mat{i}"] = None
            row[f"L{i}"] = None
            row[f"k{i}"] = None

    Q = round(delta_T / total_resistance, 2) if total_resistance > 0 else 0
    row["Q"] = Q

    data.append(row)

df = pd.DataFrame(data)
df.to_csv("heat_transfer_multilayer_dataset.csv", index=False)